
#include<stdio.h>
int main ()
{
    
    //For loop(Initialization,conditon,update)
    int i;
    for(i=1;i<=5;i++)
    {
        printf("C programming\n");
    }

    //while loop(conditon)

    int j=1;
    while (j<=5)
    {
        printf("C programming 1\n");
        j++;
    }

    //do while loop
    int k=1;
    do
    {
       printf("C programming 2\n");
       k++;
    } while (k<=5);
    
}